public class _5_Literals {
    public static void main(String[] args) {
        float f=50f;
        Double d=30.23D;
        System.out.println(f+d);

        byte b=10;
        byte b1=0b1010;//Binary
        byte b2=0145;//Octal
        byte b3=0xB;//Hexa
        System.out.println(b);
        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
    }
    
}
